class Promotion {
    constructor(idPromotion, namePromotion, descriptionPromotion, discountPromotion, startDay, endDay, status) {
        this.idPromotion = idPromotion;
        this.namePromotion = namePromotion;
        this.descriptionPromotion = descriptionPromotion;
        this.discountPromotion = discountPromotion;
        this.startDay = startDay;
        this.endDay = endDay;
        this.status = status;
    }
}
export default Promotion;